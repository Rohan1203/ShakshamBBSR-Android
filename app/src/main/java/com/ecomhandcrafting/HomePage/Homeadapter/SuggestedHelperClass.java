package com.ecomhandcrafting.HomePage.Homeadapter;

public class SuggestedHelperClass {

    int image;
    String title;

    public SuggestedHelperClass(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }
}
